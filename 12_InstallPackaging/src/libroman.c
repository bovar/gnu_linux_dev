/** Return a roman number of @p n
 * @param n arabic numeral
 * @return Roman number
 */
const char* d2r(int n) {
	return r[n];
}

/** Convert Roman number @p roman_number
 * @param roman_number Roman numeral
 * @return Arabic numeral
 */
int r2d(const char *roman_number) {
	int i;
	for (i = 1; i < NUM_OF_ROMANS; ++i) {
		if (!strcmp(roman_number, r[i])) {
			return i;
		}
	}
	return 0;
}
